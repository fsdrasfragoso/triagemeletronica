    public void autenticacao() {

        String sql = "select * from usuarios where login =? and senha =?";

        try {

            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUser.getText());
            pst.setString(2, txtSenha.getText());

            rs = pst.executeQuery();
            
            if (rs.next()) {
                
                String perfil = rs.getString(5);
                
                if(perfil.equalsIgnoreCase("Administrador")){
                   Tela_Do_Administrador ta = new Tela_Do_Administrador();
                    ta.setVisible(true);
                    this.dispose();
                
                    Tela_Do_Administrador.lblUsuario.setText(rs.getString(2));
                    Tela_Do_Administrador.txtNumID.setText(rs.getString(1)); 
                }
                else if(perfil.equalsIgnoreCase("Enfermeiro")){
//                     Tela_Do_Enfermeiro te = new Tela_Do_Enfermeiro();
  //                   te.setVisible(true);
    //                 this.dispose();
                
      //               Tela_Do_Enfermeiro.lblUsuario.setText(rs.getString(2));
        //             Tela_Do_Administrador.txtNumID.setText(rs.getString(1));
                }else{
                
      //          Tela_Do_Medico tm = new Tela_Do_Medico();
        //        tm.setVisible(true);
        //        this.dispose();
                
        //        Tela_Do_Medico.lblUsuario.setText(rs.getString(2));
        //        Tela_Do_Administrador.txtNumID.setText(rs.getString(1));
                }      
                conexao.close();
                
            } else {
                JOptionPane.showMessageDialog(null, "Usu√°rio e/ou Senha Invalido(s)");
                
                txtUser.setText(null);
                txtSenha.setText(null);
                
            }

        } catch (SQLException | HeadlessException e) {

            JOptionPane.showMessageDialog(null, e);

        }

    }

